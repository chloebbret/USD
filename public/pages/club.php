        Créé en 1956, l'US DOMESSIN n'a depuis, cessé d'existé. S'il a, au fil des années continué de grandir, c'est depuis une quinzaine d'années, avec l'arrivée de Yohann BERNARD BRET
         en tant que président que le club à vraiment évolué. Depuis, l'US DOMESSIN possède toutes les catégories d'équipes possibles : des petits U9 aux deux équipes séniors rejoint cette année, par une toute nouvelle équipe féminine.
